//! Casacore `Nutation::STANDARD` equation-of-equinoxes series.

const ARCSEC_RAD: f64 = 0.000_004_848_136_811_095_36;
const MJD_J2000: f64 = 51_544.5;
const JD_CENTURY_DAYS: f64 = 36_525.0;

/// Evaluate the equation of equinoxes used by casacore's standard nutation
/// model at the supplied epoch (MJD).
pub(crate) fn standard_equation_of_equinoxes_rad(mjd: f64) -> f64 {
    let t = (mjd - MJD_J2000) / JD_CENTURY_DAYS;
    let fundamental = FUNDAMENTAL_ARGUMENTS
        .map(|coeff| (coeff[0] + t * (coeff[1] + t * (coeff[2] + t * coeff[3]))) * ARCSEC_RAD);
    let mut longitude = 0.0;
    let mut obliquity = 0.0;
    for (index, args) in MULTIPLIER_ARGUMENTS.iter().enumerate() {
        let phase = args
            .iter()
            .zip(fundamental[1..].iter())
            .map(|(arg, value)| f64::from(*arg) * *value)
            .sum::<f64>();
        let (sin_phase, cos_phase) = phase.sin_cos();
        let mut longitude_coeff =
            f64::from(MULTIPLIER_COEFFICIENTS[index][0]) * ARCSEC_RAD * 1.0e-4;
        let mut obliquity_coeff =
            f64::from(MULTIPLIER_COEFFICIENTS[index][1]) * ARCSEC_RAD * 1.0e-4;
        if let Some(time_coeff) = TIME_DEPENDENT_COEFFICIENTS
            .iter()
            .find(|coeff| coeff[0] as usize == index)
        {
            longitude_coeff =
                (time_coeff[1] as f64 * 1.0e-4 + t * time_coeff[2] as f64 * 1.0e-5) * ARCSEC_RAD;
            obliquity_coeff =
                (time_coeff[3] as f64 * 1.0e-4 + t * time_coeff[4] as f64 * 1.0e-5) * ARCSEC_RAD;
        }
        longitude += longitude_coeff * sin_phase;
        obliquity += obliquity_coeff * cos_phase;
    }

    let nutation_longitude = -longitude;
    let true_obliquity = -fundamental[0] - obliquity;
    -nutation_longitude * true_obliquity.cos()
}

