//! Casacore `Aberration::STANDARD` orbital-velocity series.
//!
//! This module ports the coefficient tables used by casacore
//! `Measures/Aberration.cc` and `Measures/MeasTable*.cc` for the
//! geocentric/barycentric velocity hop. The public Rust measures API keeps
//! using SOFA for most direction work, but frequency/radial-velocity parity
//! with casacore requires this exact Earth orbital-velocity series.

// Casacore's `UnitVal(1e-8, "AU/d")` uses the legacy IAU light-time
// definition `C::c * 499.0047837 s`, not the modern exact AU metre value.
const AU_M: f64 = 299_792_458.0 * 499.004_783_7;
const DAY_S: f64 = 86_400.0;
const JD_CENTURY_DAYS: f64 = 36_525.0;
const MJD_J2000: f64 = 51_544.5;
const SERIES_FACTOR_MS: f64 = AU_M * 1.0e-8 / DAY_S;

/// Evaluate casacore's `Aberration::STANDARD` Earth barycentric velocity
/// series at the given TDB epoch and return the J2000 Cartesian velocity in
/// metres per second.
pub(crate) fn earth_barycentric_velocity_ms(tdb_mjd: f64) -> [f64; 3] {
    let t = (tdb_mjd - MJD_J2000) / JD_CENTURY_DAYS;
    let mut fa = [0.0; 13];
    for (i, [base, rate]) in ABER_FUND.iter().copied().enumerate() {
        fa[i] = base + rate * t;
    }

    let mut velocity_ms = [0.0; 3];

    for (row, args) in MUL_ABER_ARG.iter().enumerate() {
        let phase = args
            .iter()
            .zip(fa.iter())
            .take(6)
            .map(|(arg, fund)| f64::from(*arg) * *fund)
            .sum::<f64>();
        let (sin_phase, cos_phase) = phase.sin_cos();
        let coeff = mul_aber_coefficients(row, t);
        velocity_ms[0] += coeff[0] * sin_phase + coeff[1] * cos_phase;
        velocity_ms[1] += coeff[2] * sin_phase + coeff[3] * cos_phase;
        velocity_ms[2] += coeff[4] * sin_phase + coeff[5] * cos_phase;
    }

    for (args, coeff) in MUL_ABER_SUN_ARG.iter().zip(MUL_SUN_ABER.iter()) {
        let phase = args
            .iter()
            .zip(fa[1..8].iter())
            .map(|(arg, fund)| f64::from(*arg) * *fund)
            .sum::<f64>();
        let (sin_phase, cos_phase) = phase.sin_cos();
        let coeff = scale_six(coeff);
        velocity_ms[0] += coeff[0] * sin_phase + coeff[1] * cos_phase;
        velocity_ms[1] += coeff[2] * sin_phase + coeff[3] * cos_phase;
        velocity_ms[2] += coeff[4] * sin_phase + coeff[5] * cos_phase;
    }

    for (args, coeff) in MUL_ABER_EARTH_ARG.iter().zip(MUL_EARTH_ABER.iter()) {
        let phase = args
            .iter()
            .zip(fa[8..13].iter())
            .map(|(arg, fund)| f64::from(*arg) * *fund)
            .sum::<f64>();
        let (sin_phase, cos_phase) = phase.sin_cos();
        let coeff = scale_three(coeff);
        velocity_ms[0] += coeff[0] * sin_phase;
        velocity_ms[1] += coeff[1] * cos_phase;
        velocity_ms[2] += coeff[2] * cos_phase;
    }

    velocity_ms
}

fn mul_aber_coefficients(row: usize, t: f64) -> [f64; 6] {
    if row < 3 {
        std::array::from_fn(|index| {
            let offset = index * 3;
            let coeff = MUL_ABER_TD[row];
            let c0 = coeff[offset] as f64;
            let c1 = coeff[offset + 1] as f64;
            let c2 = coeff[offset + 2] as f64;
            (c0 + t * (c1 + t * c2)) * SERIES_FACTOR_MS
        })
    } else {
        scale_six(&MUL_ABER[row])
    }
}

fn scale_six(values: &[i16; 6]) -> [f64; 6] {
    std::array::from_fn(|index| f64::from(values[index]) * SERIES_FACTOR_MS)
}

fn scale_three(values: &[i16; 3]) -> [f64; 3] {
    std::array::from_fn(|index| f64::from(values[index]) * SERIES_FACTOR_MS)
}

